#include "../libft/libft.h"
#include "ft_printf.h"

void	print_hex(unsigned long long num, char format, int *printed)
{
	char	*base;

	if (format == 'x')
		base = "0123456789abcdef";
	else
		base = "0123456789ABCDEF";
	if (num >= 16)
		print_hex(num / 16, format, printed);
	print_char(base[num % 16], printed);
}
